import Foundation

public enum FontFamily: String {
    case rubik = "Rubik"
    case roboto = "Roboto"
    case poppins = "Poppins"
}

public enum FontWeight: String {
    case regular = "Regular"
    case medium = "Medium"
    case bold = "Bold"
    case semibold = "SemiBold"
    case italic = "Italic"
}
